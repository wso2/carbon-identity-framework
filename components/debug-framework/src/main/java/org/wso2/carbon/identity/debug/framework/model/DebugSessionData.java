package org.wso2.carbon.identity.debug.framework.model;

import java.io.InputStream;
import java.io.Serializable;

/**
 * Model class for Debug Session Data.
 */
public class DebugSessionData implements Serializable {

    private String sessionId;
    private String status;
    private InputStream sessionData; // Serialized context map
    private String resultJson;
    private long createdTime;
    private long expiryTime;

    public String getSessionId() {
        return sessionId;
    }

    public void setSessionId(String sessionId) {
        this.sessionId = sessionId;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public InputStream getSessionData() {
        return sessionData;
    }

    public void setSessionData(InputStream sessionData) {
        this.sessionData = sessionData;
    }

    public String getResultJson() {
        return resultJson;
    }

    public void setResultJson(String resultJson) {
        this.resultJson = resultJson;
    }

    public long getCreatedTime() {
        return createdTime;
    }

    public void setCreatedTime(long createdTime) {
        this.createdTime = createdTime;
    }

    public long getExpiryTime() {
        return expiryTime;
    }

    public void setExpiryTime(long expiryTime) {
        this.expiryTime = expiryTime;
    }
}
